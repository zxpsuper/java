class OperateDemo4 
{
	public static void main(String[] args) 
	{
		// 且运算
		// 0110
		// 0011
		// 0010 === 2
		System.out.println(6&3);
		// 或运算
		// 0110
		// 0011
		// 0111 === 7
		System.out.println(6|3);

		// 加一再取反 -8
		System.out.println(~7); 

		// 箭头往那边就往那边移动
		System.out.println(7<<2);//3左移两位。

	}
}
